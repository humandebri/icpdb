async function rewriteSdkImportExtensions() {
  for (const fileName of sdkModuleFiles) {
    const filePath = join(root, "dist-sdk", fileName);
    const source = await readFile(filePath, "utf8");
    const nextSource = source.replaceAll(/(from\s+["']\.\/)([^"'.]+)(["'])/g, (match, prefix, name, suffix) => {
      if (!sdkModuleNames.has(name)) {
        return match;
      }
      return `${prefix}${name}.js${suffix}`;
    });
    if (nextSource !== source) {
      await writeFile(filePath, nextSource);
    }
  }
}

